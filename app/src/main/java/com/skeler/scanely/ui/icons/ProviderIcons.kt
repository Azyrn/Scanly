    private fun logo(name: String, vararg paths: String): ImageVector {
        val builder = ImageVector.Builder(
            name = name,
            defaultWidth = 24.dp,
            defaultHeight = 24.dp,
            viewportWidth = 24f,
            viewportHeight = 24f
        )
        paths.forEach { path ->
            builder.addPath(
                pathData = addPathNodes(path),
                fill = SolidColor(Color.Black),
                pathFillType = PathFillType.EvenOdd
            )
        }
        return builder.build()
    }
